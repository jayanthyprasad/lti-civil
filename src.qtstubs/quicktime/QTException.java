package quicktime;

public class QTException extends Exception
{

	public QTException()
	{
		super();
		
	}

	public QTException(String message, Throwable cause)
	{
		super(message, cause);
		
	}

	public QTException(String message)
	{
		super(message);
		
	}

	public QTException(Throwable cause)
	{
		super(cause);
		
	}

}
